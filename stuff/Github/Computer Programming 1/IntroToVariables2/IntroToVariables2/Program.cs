using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace IntroToVariables2
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //Variable declaration - Giving a variable a name and data type
            int num1;
            //Variable assignment - Giving a variable a value
            //num1 has already been declared 
            num1 = 256;
            //Variable initialization - Declaring a variable and giving it a value
            //When using the datatype decimal you must add m to the end of the value
            decimal gasPrice = 4.89m;
            //Strings are used for text
            String studentName = "Josh";
            //char is used for one letter
            char letterGrade = 'A';
            //bool - boolean true or false only
            bool isOdd = true;
            //You only declare a variable once
            //You can reassign values of a variable
        }
    }
}
